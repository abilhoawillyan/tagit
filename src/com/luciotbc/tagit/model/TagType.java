package com.luciotbc.tagit.model;

public enum TagType {
	
	I_GIVE_UP("Desisto"),
	LOOCKS_FINE_TO_ME("Para mim está bom"),
	WHERE_I_AM("Onde estou?"),
	OOPS("Epa!"),
	I_CANT_DO_IT_THISWAY("Assim não dá!"),
	WHAT_HAPPENED("Ué, o que houve?"), 
	WHERE_IS_IT("Cadê?"),
	WHAT_IS_THIS("O que é isso?"),
	HELP("Socorro!"),
	WY_DOESENT_IT("Por que não funciona"),
	WHAT_NOW("E agora?"),
	THANKS_BUT_NO("Não, obrigado!"),
	I_CAN_DO_OTHERWIDE("Vai de outro jeito");
	
	private final String tag;
	
	private TagType(String tag){
		this.tag = tag;
	}

	public String toString() {
		return tag;
	}
	
}
